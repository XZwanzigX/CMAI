        <p class="faq-heading">General questions:</p>

        <p><em class="faq">What do I need to do to come?</em>
        <br>Block out your calender for the first week of May, and make plans to join us. Registration will be available on the site very soon.</p>

        <p><em class="faq">Do I have to know how to ride?</em>
        <br>There will be riding lessons available everyday and every level of instruction. Mounted combat classes will include an equisim ( practice horse), in case you do not feel comfortable on a horse but still wish to learn the mounted combat techniques.</p>

        <p><em class="faq">Do I have to have armor to learn to joust/fight on horseback/ fight on foot?</em>
        <br>It is not necessary to have full armor to learn the techniques of armored combat, but the more armor you can wear the better your training will be. Many of the foot techniques are based around the use of armor against ones opponent and one must work against the limitations in movement of the armored warrior.</p>

        <p><em class="faq">Will I be able to rent or borrow armor?</em>
        <br>There will be some armor available that can be borrowed, and if you have extra armor, consider bringing it as well.</p>

        <p><em class="faq">Is there a hotel that I can stay at with reduced pricing?</em>
        <br>The CMAI will be booking a block of rooms for all of our instructors students and Lysts on the Lake participants.</p>

        <p><em class="faq">Is there a meal plan?</em>
        <br>There will be a meal plan available at the event so that participants will not have to travel off site, or you are welcome to travel to any nearby eatery.</p>

