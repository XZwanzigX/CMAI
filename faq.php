            <span class="CMAI-Headline-Text">Frequently Asked Questions about CMAI</span>

            <p class="faq-heading">General questions:</p>

            <p><span class="faq">How much does the CMAI symposium cost?</span>
                <br/>$150 for foot classes<br/>
                +$100 to add the equestrian classes<br/>
                +$200 to add the meal plan (breakfast, lunch and dinner, Tuesday through Sunday)</p>

            <p><span class="faq">What do I need to do to come?</span>
                <br/>Block out your calender for the second week of May, complete the <a href="registration.php" class="textLink">registration</a> and make plans to join us!</p>

            <p><span class="faq">Are there hotels/motels nearby?</span>
                <br/>Yes, there are a number of hotels located in the Taylor and Hutto areas.  We are in the process of securing room blocks for these events which will provide a discounted rate for the participants.</p>

            <p><span class="faq">Is camping allowed on-site?</span>
                <br/>There is no tent camping space on-site but there is plenty of space for RVs and travel trailers.  However, there is a limited supply of electricity and water.  A septic truck will be available a few times during the week to clean out your tanks if needed.</p>

            <p><span class="faq">Do I have to have armor to learn to joust/fight on horseback/ fight on foot?</span>
            <br/>It is not necessary to have full armor to learn the techniques of armored combat, but the more armor you can wear the better your training will be. Many of the foot techniques are based around the use of armor against ones opponent and one must work against the limitations in movspanent of the armored warrior.</p>

            <p><span class="faq">Will I be able to rent or borrow armor?</span>
            <br/>There will be some armor available that can be borrowed, and if you have extra armor, consider bringing it as well.</p>


            <p><span class="faq">Is there a meal plan?</span>
            <br/>There will be a meal plan available at the event so that participants will not have to travel off site.  However, Taylor does have several restaurants and fast food establishments nearby, including a supermarket right next door to the event venue.</p>

            <p><span class="faq">How old do I have to be to participate?</span><br/>
            CMAI participants should be 18 years or older.</p>

            <p><span class="faq">Is there anything going on in the evening after classes?</span>
            <br/>There will socializing, armor repair, horse maintenance, as well as some possible entertainment.</p>

            <p><span class="faq">How can I help?</span>
            <br/>Please contact the event organizer, Steve Hemphill via <a href="mailto:steve@aplaisance.com" class="textLink">email</a></p>

            <p><span class="faq">What should I bring?</span>
            <br/>We all want to be prepared for anything, but here are some suggestions:<br/>
            <span>Scholars-</span> notebook, pen and pencil, camera, video, comfortable clothes, sunscreen, hat.<br/>
            <span>Footmen-</span> (everything for the scholar and) armor, arming clothes, athletic clothes, practice sword, pollaxe simulator, practice dagger, spear simulator.  Details for deed at arms will be posted on the website under rules.<br/>
            <span>Riders-</span> (everything for scholars and) Riding clothes, period clothes, boots or shoes with heel, equestrian equipment.<br/>
            <span>Mounted Combat riders-</span>(everything for scholars, footmen and riders)<br/>
            <span>Jousters-</span>  (everything for scholars, footmen and riders) and an ecranche, </p>

            <p class="faq-heading">Equestrian Questions:</p>
            <p><span class="faq">I don't have a horse.  Will there be horses available to use?</span>
            <br/>Yes.  Please indicate on the <a href="registration.php" class="textLink">registration</a> page that you are in need of a horse and we will make arrangements for you to have one during your class.

            <p><span class="faq">Can I bring my own horse?</span>
            <br/>Yes!  There are stalls available on-site with priority going to the jousting competitors in the "Lysts on the Lake". Please contact the event organizer,  <a href="mailto:steve@aplaisance.com" class="textLink">Steve Hemphill</a>, to make arrangements.</p>

            <p><span class="faq">Do I have to know how to ride?</span>
                <br/>There will be riding lessons available everyday and every level of instruction. Mounted combat classes will include an equisim ( practice horse), in case you do not feel comfortable on a horse but still wish to learn the mounted combat techniques.</p>

            <p><span class="faq">Do I need armour to take advantage of the riding classes?</span>
            <br/>Not necessarily.  The Beginning Riding track will not require any armour.  The Jousting track consists of 5 classes of which the first 2 do not require armour.  The final 3, however, do.

